package MATH;
public class EjercicioMath4 {
    public static void main(String[] args) {
        
        // Diferencias entre Round, ceil, floor

        final float NUMERO_1 = 0.0234f;
        final float NUMERO_2 = 0.867f;

        System.out.println(Math.round(NUMERO_1));
        System.out.println(Math.round(NUMERO_2));

        System.out.println(Math.ceil(NUMERO_1));
        System.out.println(Math.ceil(NUMERO_2));

        System.out.println(Math.floor(NUMERO_1));
        System.out.println(Math.floor(NUMERO_2));


    }
}
