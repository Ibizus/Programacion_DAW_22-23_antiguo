package MATH;
public class tipoPrimitivo 
{
    public static void main(String[] args)
    {
        byte numero = 127;
        System.out.println(numero);

        numero++;
        System.out.println(numero);

        short numero2 = 32767;
        System.out.println(numero2);

        numero2++;
        System.out.println(numero2);

        String saludo = "Hola";
        System.out.println(saludo);
    }
}
