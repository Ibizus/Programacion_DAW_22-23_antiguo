package CLASES_T6.Clases;

public class Punto {

    // ATRIBUTOS:
    private double x;
    private double y;
    
    // CONSTRUCTORES:
    public Punto(double x, double y)
    {
        this.x = x;
        this.y = y;
    }

    // METODOS:
    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
    
}
